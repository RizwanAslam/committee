     */
    public function store(Request $request)
    {
        $company_id = $request->route('id');
        $member = Member::find(auth()->user()->id);
        $member->update([
            'company_id' => $company_id,
        ]);
        session(['company_id' => $company_id]);
        return redirect()->route('dashboard.index');
    }

